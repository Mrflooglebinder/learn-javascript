// For loop vs while loop
// in a for loop the loop variable is defined in the loop itself
// in a while loop the loop variable is defined outside the loop

// While loop - Basic syntax
// let i = 0;
// while (condition) {
//     statement
//     i++;
// }

let i = 0;
while (i <= 5) {
    if (i % 2 !== 0) console.log(i)
    i++;
}
